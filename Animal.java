public class Animal {
    private int x =3;
    private int y=4;
    private int z=5;
    public static void main(String[] args)
    {
        Animal pri = new Animal();
        System.out.println("the first private variable is"+pri.x);
        System.out.println("the Second private variable is"+pri.y);
        System.out.println("the third private variable is"+pri.z);
    }
}


